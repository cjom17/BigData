    private void LOGINBTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LOGINBTNMouseClicked
        String username = UNAME.getText();
        String password = UPASS.getText();
        
        
        
        
       
        
        if (username.equals("")  ) {
            JOptionPane.showMessageDialog(null, "Username is required");
            
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(null, "Password is required");
//           
//            MongoCursor<Document> query = mc.find(identify).iterator();
        }
     
    
        
        
        else{
            MongoClient client = MongoClients.create("mongodb+srv://mel:1234@cluster0.tz2saex.mongodb.net/?retryWrites=true&w=majority");
            MongoDatabase db = client.getDatabase("BigData");
            MongoCollection mc = db.getCollection("users");
             BasicDBObject identify = new BasicDBObject();
             identify.put("upass", UPASS.getText());
             identify.put("uname", UNAME.getText());
             
            MongoCursor<Document> mcr = mc.find(identify).iterator();
         
            if (mcr.hasNext()) {
                JOptionPane.showMessageDialog(this, "Login Successfully!");
                dashboard dash = new dashboard();
                dash.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "User not found");
            }
        }
    
     
    }//GEN-LAST:event_LOGINBTNMouseClicked

